console.log(Math.random()); //0~1.0未満の乱数がランダムで表示
console.log(Math.ceil(5.4)); //6 引数の値を整数にすることができます。小数を繰り上げることで整数にします。
console.log(Math.floor(5.4)); //5 引数の値を整数にすることができます。少数を繰り下げることで整数にします。
console.log(Math.round(5.4)); //5 引数の値を整数にしますが、少数を四捨五入することで整数にします。
console.log(Math.floor(Math.random() * 20)); //0~19のうちの整数の乱数を生成